export function getStopBySlug(slug: string): TourStop | undefined {
  return mockStops.find((stop) => stop.slug === slug);
}

export function getStopById(id: string): TourStop | undefined {
  return mockStops.find((stop) => stop.id === id);
}

export function getAllStops(): TourStop[] {
  return mockStops;
}

export function getStopsByCategory(category: string): TourStop[] {
  return mockStops.filter((stop) => stop.category === category);
}

export function getStopsByIds(ids: string[]): TourStop[] {
  return ids.map((id) => getStopById(id)).filter((stop): stop is TourStop => Boolean(stop));
}
